use Illuminate\Auth\Authenticatable;
use Illuminate\Contracts\Auth\Authenticatable as AuthenticatableContract;

class User extends Model implements AuthenticatableContract
{
    use Authenticatable;
    use Notifiable;
    use HasFactory;

    public $timestamps = false;

    protected $fillable =[
        'email',
        'password',
    ];
}
